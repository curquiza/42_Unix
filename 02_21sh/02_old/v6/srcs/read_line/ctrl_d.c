#include "shell.h"

void	ft_ctrl_d(t_tc *tool)
{
	if (tool->nbr_char == 0)
	{
		g_shell->run = 0;
		ft_bzero(tool->buff, TC_SIZE);
		tool->stop = 1;
	}
}
